def get_south_india_regions():
    """Get all South India regions"""
    return SOUTH_INDIA_REGIONS

def get_location_variations():
    """Get location variations and aliases"""
    return LOCATION_VARIATIONS

def get_region_coordinates(region_name):
    """Get coordinates for a specific region"""
    region_lower = region_name.lower().strip()
    
    # Check direct match
    if region_lower in SOUTH_INDIA_REGIONS:
        return SOUTH_INDIA_REGIONS[region_lower]
    
    # Check variations
    if region_lower in LOCATION_VARIATIONS:
        mapped_region = LOCATION_VARIATIONS[region_lower]
        if mapped_region in SOUTH_INDIA_REGIONS:
            return SOUTH_INDIA_REGIONS[mapped_region]
    
    # Try partial matching
    for key, coords in SOUTH_INDIA_REGIONS.items():
        if region_lower in key or key in region_lower:
            return coords
    
    # Default to Chennai if not found
    return SOUTH_INDIA_REGIONS['chennai']

def get_region_count():
    """Get total number of regions"""
    return len(SOUTH_INDIA_REGIONS)

